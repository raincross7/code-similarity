const int NMAX = 1e5;
vec edge[NMAX], g(NMAX,-1), used(NMAX,0);
void dfs(int no){
  int grundy = 0;
  used[no]++;
  for(int to:edge[no]){
    if(used[to]) continue;
    dfs(to);
    grundy ^= (g[to]+1);
  }
  g[no] = grundy;
}

