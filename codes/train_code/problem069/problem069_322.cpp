#include <iostream>
using namespace std;

int main(){
  char c;
  cin >> c;
  switch(c){
    case 'A':
      cout << "T";
      return 0;
    case 'C':
      cout << "G";
      return 0;
    case 'G':
      cout << "C";
      return 0;
    case 'T':
      cout << "A";
      return 0;
  }
  
}
      
      
