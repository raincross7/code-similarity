#include <bits/stdc++.h>
using namespace std;

int main() {
  char t;
  cin >> t;
  if (t == 'A') {
    cout << "T";
    return 0;
  } else if (t == 'T') {
    cout << "A";
    return 0;
  } else if (t == 'C') {
    cout << "G";
    return 0;
  } else {
    cout << "C";
    return 0;
  }
  return 0;
}
