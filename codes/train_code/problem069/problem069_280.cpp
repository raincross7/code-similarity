#include <bits/stdc++.h>
using namespace std;
int main() {
  char s;
  cin >> s;
  switch (s) {
    case 'A':
      cout << "T" << endl;
      break;
    case 'C':
      cout << "G" << endl;
      break;
    case 'G':
      cout << "C" << endl;
      break;
    case 'T':
      cout << "A" << endl;
      break;
  }
}