#include <bits/stdc++.h>
using namespace std;

signed main() {
    char b;
    cin >> b;
    
    if ( b == 'A' ) {
        cout << "T\n";
    } else if ( b == 'T' ) {
        cout << "A\n";
    } else if ( b == 'C') {
        cout << "G\n";
    } else {
        cout << "C\n";
    }
    return (0);
}