#include <iostream>
#include <sstream>
#include <cmath>
#include <iomanip>

int main(int argc, char const *argv[]) {
  std::string Param;
  std::istringstream iss;
  double a, b, c;
  unsigned short C;
  double S, L, h;
  const double Pi = atan((double)INFINITY) * 2;

  getline(std::cin, Param);
  iss.str(Param);
  iss >> a >> b >> C;

  double Radians = C * Pi / 180;
  c = sqrt(pow(a, 2) + pow(b, 2) - 2 * a * b * cos(Radians));
  L = a + b + c;
  h = sin(Radians) * b;
  S = a * h / 2;
  std::cout << std::fixed << std::setprecision(8);
  std::cout << S << std::endl;
  std::cout << L << std::endl;
  std::cout << h << std::endl;

  return 0;
}