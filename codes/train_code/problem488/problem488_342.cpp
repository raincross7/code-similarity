#include <iostream>

int main()
{
    int N, K;
    std::cin >> N >> K;
    long long count = 0;
    for (long long i = K; i <= N + 1; i++)
    {
        long long min = i * (i - 1) / 2;
        long long max = N * i - min;
        count = (count + max - min + 1) % 1000000007;
        // * [0, 1, 2, 3, 4, 5, 6] ここから2個
        // (0, 1) : 1
        // (0, 2) : 2
        // (0, 3), (1, 2): 3
        // (0, 4), (1, 3): 4
        // (0, 5), (1, 4), (2, 3) : 5
        // (0, 6), (1, 5), (2, 4) : 6
        // (1, 6), (2, 5), (3, 4) : 7
        // (2, 6), (3, 5): 8
        // (3, 6), (4, 5): 9
        // (4, 6): 10
        // (5, 6): 11 = (6-1) + 6
        // 11通り
        // 2N - 1

        // [0, 1, 2, 3, 4, 5, 6] ここから3個
        // (0, 1, 2): 3
        // (0, 1, 3): 4
        // (0, 1, 4), (0, 2, 3):5
        // (0, 1, 5), (1, 2, 3), (0, 2, 4): 6 = 1 + 5 = 2 + 4
        // (0, 1, 6), (0, 2, 5), (0, 3, 4), (1, 2, 4):7
        // ..
        // (4, 5, 6) : 15 = (6-2) + (6-1) + (6-0)
        // 13通り
    }
    std::cout << count << std::endl;
}