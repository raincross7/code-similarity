//Dijkstra
template<class T> class Dijkstra {
public:
	vector<T> cost; //startからの距離
	vector<vector<pair<T, int>>> edge;//fromからtoへの<T>costの辺 

	Dijkstra(const int N, T inf) : cost(N, inf), edge(N) {
	}

	void make_edge(int from, int to, T w) {
		edge[from].push_back({ w,to });
	}

	void solve(int start) {
		priority_queue<pair<T, int>, vector<pair<T, int>>, greater<pair<T, int>>> pq;
		pq.push({ 0,start });
		cost[start] = 0;
		while (!pq.empty()) {
			T v = pq.top().first;
			int from = pq.top().second;
			pq.pop();
			for (auto u : edge[from]) {
				T w = v + u.first;
				int to = u.second;
				if (w < cost[to]) {
					cost[to] = w;
					pq.push({ w,to });
				}
			}
		}
		return;
	}
};

