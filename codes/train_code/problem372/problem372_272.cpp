int main() {
  int64_t N, t, m;
  cin >> N;
  m = N;
  for (int64_t i = (int)sqrt(N); i > 0; i--) {
    if (N % i == 0) {
      t = N / i;
      cout << i + t - 2 << endl;
      break;
    }
  }
}
