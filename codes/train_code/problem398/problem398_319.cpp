using namespace std;
typedef long long ll;
typedef unsigned long long ull;

int main() {
  ll K, S, c = 0, z;
  cin >> K >> S;

  for (int x = 0; x <= min(K, S); x++) {
    for (int y = 0; y <= min(K, S - x); y++) {
      z = S - x - y;
      if (0 <= z && z <= K) c++;
    }
  }
  cout << c << endl;
}
