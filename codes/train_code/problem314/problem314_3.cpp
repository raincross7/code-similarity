vector<ll> hoge{1};
map<ll, ll> memo;

ll dfs(ll x) {
    if(x == 0) return memo[x] = 0;
    if(x < 0) return memo[x] = numeric_limits<ll>::max() / 2;
    ll res = numeric_limits<ll>::max() / 2;
    iter(i, hoge) {
        ll key = x - i;
        if(memo.count(key) == 0) res = min(res, dfs(key) + 1);
        else res = min(res, memo[key] + 1);
    }
    return memo[x] = res;
}

