#include <bits/stdc++.h>
using namespace std;

map<long long,int> dy;
int n;
long long d[100005];
int sz[100005];
vector<int> son[100005];
long long sum=0;
int dep[100005];
vector<pair<int,int> > ans;

void dfs(int now,int lst){
	dep[now]=dep[lst]+1;
	sum+=dep[now];
	for(int i=0;i<son[now].size();i++){
		int to=son[now][i];
		if(to==lst){
			continue;
		}
		dfs(to,now);
	}
}

int main(){
	scanf("%d",&n);
	for(int i=1;i<=n;i++){
		scanf("%lld",d+i);
		dy[d[i]]=i;
		sz[i]=1;
	}
	while(dy.size()>1){
		pair<long long,int> X=*dy.rbegin();
		int x=X.second;
		long long val=X.first;
		dy.erase(val);
		long long tov=val-n+2*sz[x];
		if(!dy.count(tov)){
			puts("-1");
			return 0;
		}else{
			int ff=dy[tov];
			son[ff].push_back(x);
			son[x].push_back(ff);
			sz[ff]+=sz[x];
			ans.emplace_back(ff,x);
		}
	}
	dep[0]=-1;
	dfs(dy.begin()->second,0);
	if(sum!=dy.begin()->first){
		puts("-1");
		return 0;
	}
	for(int i=0;i<n-1;i++){
		printf("%d %d\n",ans[i].first,ans[i].second);
	}
	return 0;
}