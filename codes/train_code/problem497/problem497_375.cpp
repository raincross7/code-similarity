int dfs(int no, int par = -1, int d = 0){
  int res = 0; sum[no] = 1;
  for(int to:edge[no]){
    if(to==par) continue;
    res += dfs(to,no,d+1);
    sum[no] += sum[to];
  }
  return res+d;
}

void dfs2(int no, int par = -1){
  for(int to:edge[no]){
    if(to==par) continue;
    int x = sum[no]-sum[to];
    dist[to] = dist[no] + x - sum[to];
    sum[no] -= x; sum[to] += x;
    dfs2(to,no);
    sum[no] += x; sum[to] -= x;
  }
}

