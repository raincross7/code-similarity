#include <bits/stdc++.h>
using namespace std;
 
int main() {
  int n;
  cin >> n;
  if (n >= 1800) { cout << 1 << endl; return 0; }
  if (n >= 1600)  { cout << 2 << endl; return 0; }
  if (n >= 1400) { cout << 3 << endl; return 0; }
  if (n >= 1200) { cout << 4 << endl; return 0; }
  if (n >= 1000) { cout << 5 << endl; return 0; }
  if (n >= 800)  { cout << 6 << endl; return 0; }
  if (n >= 600)  { cout << 7 << endl; return 0; }
  if (n >= 400)  { cout << 8 << endl; return 0; }
  return 0;
}