#define INF 2e9
#define ALL(v) v.begin(), v.end()
using namespace std;
typedef long long ll;

vector<pair<ll, ll> > prime_factorize(ll N) {
    vector<pair<ll, ll> > res;
    for (ll a = 2; a * a <= N; ++a) {
        if (N % a != 0) continue;
        ll ex = 0; // 指数

        // 割れる限り割り続ける
        while (N % a == 0) {
            ++ex;
            N /= a;
        }

        // その結果を push
        res.push_back({a, ex});
    }

    // 最後に残った数について
    if (N != 1) res.push_back({N, 1});
    return res;
}

int main() {
    ll N;
    cin >> N;
  	int ans = 0;
    const auto &res = prime_factorize(N);
  
    ll z; 
    for (auto p : res) {
      z = 1;
    	for(int i = 1; i <= p.second ; i++){
		  z *= p.first;
          if(N % z == 0){
            ans++;
            N /= z;
          }
        }
    }
    cout << ans <<endl;
}