#include <bits/stdc++.h>
using namespace std;

int main() {
  int N;
  cin >> N;
  int fN;
  int a,b,c,d,e,f,g,h,i;
  a=N/100000000;
  b=N%100000000/10000000;
  c=N%100000000%10000000/1000000;
  d=N%100000000%10000000%1000000/100000;
  e=N%100000000%10000000%1000000%100000/10000;
  f=N%100000000%10000000%1000000%100000%10000/1000;
  g=N%100000000%10000000%1000000%100000%10000%1000/100;
  h=N%100000000%10000000%1000000%100000%10000%1000%100/10;
  i=N%100000000%10000000%1000000%100000%10000%1000%100%10;
  fN=a+b+c+d+e+f+g+h+i;
  if(N%fN==0){
    cout << "Yes" << endl;
  }else{
   cout << "No" << endl; 
  }
}
