#include <bits/stdc++.h>
using namespace std;

int n,k;
int h[305];
long long dp[305][305];

int main(){
	scanf("%d%d",&n,&k);
	for(int i=1;i<=n;i++){
		scanf("%d",h+i);
	}
	memset(dp,0x3f,sizeof(dp));
	dp[0][0]=0;
	for(int i=0;i<=n;i++){
		for(int j=0;j<=min(i,k);j++){
			for(int k=i+1;k<=n+1;k++){
				if(j+k-i-1<=::k){
					dp[k][j+k-i-1]=min(dp[k][j+k-i-1],dp[i][j]+max(0,h[k]-h[i]));
				}
			}
		}
	}
	long long ans=0x3f3f3f3f3f3f3f3fll;
	for(int i=0;i<=k;i++){
		ans=min(ans,dp[n+1][i]);
	}
	printf("%lld\n",ans);
	return 0;
}