#define ll long long int

ll mod=1e9+7;

int main(){
    int n,m;
    scanf("%d%d",&n,&m);
    vector<bool> inc(n+1,true);
    vector<int> dp(n+1);
    for(int i=0;i<m;i++){
        int temp;
        scanf("%d",&temp);
        inc[temp]=false;
    }
    dp[0]=1;
    dp[1]=inc[1];
    for(int i=2;i<=n;i++){
        if(inc[i]){
            dp[i]=dp[i-1]+dp[i-2];
        }
        dp[i]%=mod;
    }
    cout<<dp[n]<<"\n";
    return 0;
}
