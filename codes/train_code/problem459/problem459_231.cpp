#define debugArray(x, n) (void(0))
#endif

signed main() {
  cin.tie(0);
  ios::sync_with_stdio(false);
  long long n;
  cin >> n;
  if (n & 1)
    cout << 0 << '\n';
  else {
    n /= 2;
    long long cnt = 0;
    long long pw5 = 5;
    while (n >= pw5) cnt += n / pw5, pw5 *= 5;
    cout << cnt << '\n';
  }
  return 0;
}
