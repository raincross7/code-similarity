#include <bits/stdc++.h>
using namespace std;

int main(){
  int N;
  cin >> N;
  if(888<N){
    cout << "999" << endl;
  }else if(777<N){
    cout << "888" << endl;
  }else if(666<N){
    cout << "777" << endl;
  }else if(555<N){
    cout << "666" << endl;
  }else if(444<N){
    cout << "555" << endl;
  }else if(333<N){
    cout << "444" << endl;
  }else if(222<N){
    cout << "333" << endl;
  }else if(111<N){
    cout << "222" << endl;
  }else if(99<N){
    cout << "111" << endl;
  }
}
