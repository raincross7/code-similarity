#include <iostream>
#include <sstream>

int main(int argc, char const *argv[]) {
  unsigned int H, W;

  while (true) {
    while (true) {
      std::string Param;

      getline(std::cin, Param);
      std::istringstream iss(Param);
      iss >> H >> W;

      if (H > 300 || W > 300) {
        std::cout << "Error: 1 <= H, W <= 300" << std::endl;
      } else {
        break;
      }
    }
    if (H == 0 && W == 0) {
      break;
    } else {
      for (size_t i = 0; i < H; i++) {
        for (size_t j = 0; j < W; j++) {
          std::cout << "#";
        }
        std::cout << std::endl;
      }
      std::cout << std::endl;
    }
  }
  return 0;
}