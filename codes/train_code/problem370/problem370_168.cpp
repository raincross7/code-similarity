template<class Abel> struct UnionFind {
    vector<int> par;
    vector<int> rank;
    vector<Abel> diff_weight;

    UnionFind(int n = 1, Abel SUM_UNITY = 0) {
        init(n, SUM_UNITY);
    }

    void init(int n = 1, Abel SUM_UNITY = 0) {
        par.resize(n + 1); rank.resize(n + 1); diff_weight.resize(n + 1);
        for (int i = 1; i <= n; ++i) par[i] = i, rank[i] = 0, diff_weight[i] = SUM_UNITY;
    }

    int root(int x) {
        if (par[x] == x) {
            return x;
        }
        else {
            int r = root(par[x]);
            diff_weight[x] += diff_weight[par[x]];
            return par[x] = r;
        }
    }

    Abel weight(int x) {
        root(x);
        return diff_weight[x];
    }

    bool issame(int x, int y) {
        return root(x) == root(y);
    }

    bool merge(int x, int y, Abel w) {
        w += weight(x); w -= weight(y);
        x = root(x); y = root(y);
        if (x == y) return false;
        if (rank[x] < rank[y]) swap(x, y), w = -w;
        if (rank[x] == rank[y]) ++rank[x];
        par[y] = x;
        diff_weight[y] = w;
        return true;
    }

    Abel diff(int x, int y) {
        return weight(y) - weight(x);
    }
};

int main() {
    //cout.precision(10);
    UnionFind<int> field;
    int n, m;
    cin >> n >> m;
    field.init(n);
    for(int i = 0; i < m; i++){
        int l, r, d;
        cin >> l >> r >> d;
        if(field.issame(l, r)){
            if(field.diff(l, r) != d){
                cout << "No" << endl;
                return 0;
            }
            continue;
        }
        field.merge(l, r, d);
    }
    cout << "Yes" << endl;
    return 0;
}
