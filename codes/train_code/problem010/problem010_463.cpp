#include <bits/stdc++.h>
using namespace std;
int main()
{
	static long long m1,m2;
	int N;
	cin>>N;
  vector<long long> v(N);
	for(int i=0;i<N;i++) cin>>v[i];
	sort(v.begin(),v.end(),greater<int>());
	for(int i=0;i<N;i++){
		if(v[i]==v[i+1]&&m1==0){m1=v[i],i++;}
		else if(v[i]==v[i+1]&&m2==0){m2=v[i],i++;}
		else if(m1&&m2) i=N;
	}
	cout<<m1*m2<<endl;
}