#define N 5005
#define ll long long
// #define mod 23333333333333333ll
int n,a[N],num,K;ll f[N];
// bool check(double x,double y){if(abs(x-y)<1e-6)return 1;return 0;}
bool check(int i)
{
	int ret=0;
	for(int j=a[i];j<=K;j++)f[j]=f[j]-f[j-a[i]];
	for(int j=K-1;j>=K-a[i];j--)
		if(f[j])ret=1;
	for(int j=K;j>=a[i];j--)f[j]=f[j]+f[j-a[i]];
	return ret;
}
int main()
{
	// freopen("Num.in","r",stdin);freopen("Num.out","w",stdout);
	scanf("%d%d",&n,&K);
	for(int i=1;i<=n;i++){scanf("%d",&a[i]);if(a[i]>=K)i--,n--;}
	f[0]=1;
	for(int i=1;i<=n;i++)
		for(int j=K;j>=a[i];j--)
			f[j]=f[j]+f[j-a[i]];
	// printf("%.0f %.0f %.0f\n",f[K],f[K-1],f[K-2]);
	for(int i=1;i<=n;i++)if(!check(i))num++;
	printf("%d\n",num);
}
