const int MAX = 1000010;
const int MOD = pow(10, 9) + 7;

ll fac[MAX], finv[MAX], inv[MAX];

// テーブルを作る前処理
void COMinit() {
    fac[0] = 1;
    fac[1] = 1;
    finv[0] = 1;
    finv[1] = 1;
    inv[1] = 1;
    for (int i = 2; i < MAX; i++){
        fac[i] = fac[i - 1] * i % MOD;
        inv[i] = MOD - inv[MOD%i] * (MOD / i) % MOD;
        finv[i] = finv[i - 1] * inv[i] % MOD;
    }
}

// 二項係数計算
ll COM(int n, int k){
    if (n < k) return 0;
    if (n < 0 || k < 0) return 0;
    return fac[n] * (finv[k] * finv[n - k] % MOD) % MOD;
}

int main() {
  int X, Y; cin >> X >> Y;
  ll ans = 0;
  COMinit();
  ll n = (2 * Y - X) / 3;
  ll m = (2 * X - Y) / 3;
  if((X + Y) % 3 != 0) {
    ans = 0;
  } else {
    ans = COM(n + m, n);
  }
  cout << ans << endl;
}
