int main(){
    int n,k;
    scanf("%d%d",&n,&k);
    double win=0;
    double num_prob=(double)(1)/n;
    for(int i=1;i<=n;i++){
        if(i>=k){
            win+=(double)(1)/n;
        }
        else{
            int t=i;
            int cnt=0;
            while(t<k){
                t*=2;
                cnt++;
            }
            double steps=pow(2,cnt);
            double prob=(double)(1)/steps;
            win=win+(num_prob)*(prob);
        }
    }
    printf("%0.15f\n",win);
    return 0;
}
