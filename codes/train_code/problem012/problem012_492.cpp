typedef pair<long long int, long long int> pll;


const long long int mod = 1000000007;
const long long int INF = 1e18;
const long double PI=3.14159265358979323; 

long long int N,W,H,x[200],b[203230],a[203230];
long long int U,D=0,L=0,R;
priority_queue<pll>pq;

int main()
{
    cout << fixed << setprecision(18);
    cin>>N>>H;
    for(long long int i = 0; i < N; i++){
        cin>>a[i]>>b[i];
        pq.push({b[i],1});
        pq.push({a[i],INF});
    }
    long long int T=0;
    while (H>0)
    {
        pll tmp=pq.top();
        pq.pop();
        if(tmp.second==1){
            T++;
            H-=tmp.first;
        }else{
            T+=(H+tmp.first-1)/tmp.first;
            H=0;
        }
    }
    cout<<T<<endl;
}