void debug_out() { std::cerr << '\n'; }

template <class Head, class... Tail>
void debug_out(Head&& head, Tail&&... tail) {
  std::cerr << head;
  if (sizeof...(Tail) != 0) std::cerr << ", ";
  debug_out(std::forward<Tail>(tail)...);
}
using namespace std;
using int64 = long long;

int main() {
  int N;
  cin >> N;
  vector<int64> a(N), b(N);
  cin >> a >> b;
  int64 addone = 0;
  int64 addtwo = 0;
  for (int i = 0; i < N; i++) {
    if (a[i] == b[i]) continue;
    if (a[i] > b[i]) {
      addone += a[i] - b[i];
    } else {
      if ((b[i] - a[i]) % 2 == 0) {
        addtwo += (b[i] - a[i]) / 2;
      } else {
        addone++;
        addtwo += (b[i] + 1 - a[i]) / 2;
      }
    }
  }
  debug(addone, addtwo);
  if (addone > addtwo) {
    cout << "No" << endl;
  } else {
    cout << "Yes" << endl;
  }
  return 0;
}