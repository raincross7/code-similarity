    inline char readalpha() {
        char dd;
        for (; !isalpha(c); dd);
        return c;
    }

    inline char readchar() {
        char dd;
        for (; c == ' '; dd);
        return c;
    }

    template <class T>
    inline bool read(T& x) {
        bool flg = false;
        char dd;
        x = 0;
        for (; !isdigit(c); dd) {
            if (c == '-') {
                flg = true;
            } else if(c == EOF) {
                return false;
            }
        }
        for (; isdigit(c); dd) {
            x = (x << 1) + (x << 3) + (c ^ 48);
        }
        if (flg) {
            x = -x;
        }
        return true;
    }
#undef dd

    template <class T>
    inline void write(T x) {
        if (x < 0) {
            putchar('-');
            x = -x;
        }
        if (x < 10) {
            putchar(x | 48);
            return;
        }
        write(x / 10);
        putchar((x % 10) | 48);
    }

    template <class T>
    inline void writesp(T x) {
        write(x);
        putchar(' ');
    }

    template <class T>
    inline void writeln(T x) {
        write(x);
        puts("");
    }
    
    typedef long long LL;
    
    const int maxn = 100005;
    
    int n;
    LL x[maxn];
    LL y[maxn];
    LL a[maxn];
    
    int main() {
        read(n);
        for (int i = 0; i <= n; ++i) {
            read(x[i]);
            y[i] = x[i];
        }
        for (int i = n; ~i; --i) {
            x[i] += x[i + 1];
        }
        if (n && y[0]) {
            puts("-1");
            return 0;
        }
        if (!n && y[0] != 1) {
            puts("-1");
            return 0;
        } else if (!n) {
            puts("1");
            return 0;
        }
        LL ans = 1, tmp = 1;
        for (int i = 1; i <= n; ++i) {
            tmp <<= 1;
            tmp -= y[i];
            if (tmp <= 0 && i != n) {
                puts("-1");
                return 0;
            }
            tmp = min(x[i + 1], tmp);
            ans += tmp + y[i];
        }
        if (tmp) {
            puts("-1");
            return 0;
        }
        writeln(ans);
        return 0;
    }
