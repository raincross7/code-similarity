void bfs(int u){

    queue<int> Q;
    Q.push(u);
    cnt[u] = 0;
    int v = Q.front();

    while(!Q.empty()){

        for(int i=0; i<n; i++){
            if(m[v][i] == 1 && cnt[i] == -1){
                Q.push(i);
                cnt[i] = cnt[v]+1;
            }
        }

        Q.pop();
        v = Q.front();
    }
}


