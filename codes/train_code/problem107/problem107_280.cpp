#include <iostream>
using namespace std;

int main()
{
	int w, h;			//w???h?????????
	int n;				//??\?????????????????´???????????°
	int x[40], y[40];	//?????????(x[i], y[i])???????????????
	int i, j;
	
	while( cin >> w >> h ){
		if( w == 0 && h == 0 )
			break;
		
		int isNG[41][41] = {0};		//isNG[i][j] = ?????????(i, j)?????????????????´???1
		int dp[41][41] = {0};		//dp[i][j] = ?????????(1, 1)???????????????(i, j)?????????????????°
		
		cin >> n;
		for( i = 0; i < n; i++ ){
			cin >> x[i] >> y[i];
			isNG[x[i]][y[i]] = 1;
		}
		
		dp[1][1] = 1;
		for( i = 1; i <= w; i++ ){
			for( j = 1; j <= h; j++ ){
				if( i == 1 && j == 1 )
					continue;
				if( isNG[i][j] == 1 )
					continue;
				dp[i][j] = dp[i-1][j] + dp[i][j-1];
			}
		}
		cout << dp[w][h] << endl;
	}
	return 0;
}