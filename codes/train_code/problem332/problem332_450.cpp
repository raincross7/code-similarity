const int N = 1e5+10;
vector<int> edge[N],a(N);

int dfs(int no, int par){
  int sum = 0, mx = 0;
  for(int to: edge[no]){
    if(to==par) continue;
    int k = dfs(to,no);
    if(k==-1) return -1;
    sum += k; chmax(mx,k);
  }
  if(edge[no].size()==1) return a[no];
  else if(!range(a[no],max((sum+1)/2,mx),sum+1)) return -1;
  else return a[no]-(sum-a[no]);
}

