const int NMAX = 1e5;
vec used(NMAX,0),edge[NMAX],a(NMAX);
int check(int no){
  used[no]++;
  int sum = 0,cnt = 0,mx = 0;
  for(int to:edge[no]){
    if(used[to]) continue;
    int tmp = check(to);
    cnt++;
    sum += tmp;
    mx = max(mx,tmp);
  }
  if(cnt == 0) return a[no];
  if(no == 0){
    if((cnt == 1 && sum == a[no]) || (cnt > 1 && sum == a[no]*2 && mx*2 <= sum)) return 0;
    else{
      cout << "NO" << endl;
      exit(0);      
    }
  }
  else{
    int cur = 2*a[no] - sum;
    if(sum >= a[no] && min((sum-mx)*2,sum) >= sum-cur) return cur;
    else{
      cout << "NO" << endl;
      exit(0);
    }    
  }
}

