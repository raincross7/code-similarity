#include <iostream>
#include <string>
#include <algorithm>

using namespace std;

int main() {
  int T;
  cin >> T;
  for (int t=0; t<T; t++) {
    string word1, word2;
    cin >> word1 >> word2;
    int m = word1.size();
    int n = word2.size();
    int c[m+1][n+1]={};
    int max_len = 0;
    word1 = ' ' + word1;
    word2 = ' ' + word2;

    for (int i=1; i<=m; i++) {
      for (int j=1; j<=n; j++) {
        if (word1[i] == word2[j]) {
          c[i][j] = c[i-1][j-1] + 1;
        }
        else {
          c[i][j] = max(c[i-1][j], c[i][j-1]);
        }
        max_len = max(max_len, c[i][j]);
      }
    }
    cout << max_len << endl;
  }
  return 0;
}

