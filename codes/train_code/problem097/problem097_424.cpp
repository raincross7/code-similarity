using namespace std;
typedef long long ll;
typedef unsigned long long ull;

int main() {
  ll H, W;
  cin >> H >> W;
  if (H == 1 || W == 1) {
    cout << 1 << endl;
    return 0;
  }

  if (H % 2 == 0 || W % 2 == 0) {
    cout << H * W / 2 << endl;
    return 0;
  }

  cout << H * W / 2 + 1 << endl;
}
