#include <bits/stdc++.h>
using namespace std;
using ll=long long;
int main() {
  ll a;
  cin>>a;
  if(a==25){
    cout<<"Christmas"<<endl;
    return 0;
  }
  if(a==24){
    cout<<"Christmas Eve"<<endl;
    return 0;
  }
  if(a==23){
    cout<<"Christmas Eve Eve"<<endl;
    return 0;
  }
  if(a==22){
    cout<<"Christmas Eve Eve Eve"<<endl;
  }
}
    
    
  
