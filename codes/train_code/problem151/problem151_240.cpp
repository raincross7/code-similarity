#include<bits/stdc++.h>
using namespace std;
int main(){
  int D;
  cin>>D;
  if(D==25){
    cout<<"Christmas"<<endl;
    return 0;
  }
  if(D==24){
    cout<<"Christmas Eve"<<endl;
    return 0;
  }
  if(D==23){
    cout<<"Christmas Eve Eve"<<endl;
    return 0;
  }
  if(D==22){
    cout<<"Christmas Eve Eve Eve"<<endl;
    return 0;
  }
}