#include <bits/stdc++.h>
using namespace std;

int main() {
  int N;
  cin >> N;
  if (N == 25) return cout << "Christmas" << "\n", 0;
  if (N == 24) return cout << "Christmas Eve" << "\n", 0;
  if (N == 23) return cout << "Christmas Eve Eve" << "\n", 0;
  if (N == 22) return cout << "Christmas Eve Eve Eve" << "\n", 0;
}