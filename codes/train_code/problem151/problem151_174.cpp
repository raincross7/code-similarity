#include<bits/stdc++.h>
using namespace std;

int main(void){
  int D;
  cin >> D;
  if(D == 25)
    cout << "Christmas" << "\n";
  else if(D == 24)
    cout << "Christmas Eve" << "\n";
  else if(D == 23)
    cout << "Christmas Eve Eve" << "\n";
  else if(D == 22)
    cout << "Christmas Eve Eve Eve" << "\n";
  return 0;
}
