const double pi=3.141592653589793;

struct UnionFind {
    vector<int> par;
    
    UnionFind(int n) : par(n, -1) { }
    void init(int n) { par.assign(n, -1); }
    
    int root(int x) {
        if (par[x] < 0) return x;
        else return par[x] = root(par[x]);
    }
    
    bool issame(int x, int y) {
        return root(x) == root(y);
    }
    
    bool merge(int x, int y) {
        x = root(x); y = root(y);
        if (x == y) return false;
        if (par[x] > par[y]) swap(x, y); // merge technique
        par[x] += par[y];
        par[y] = x;
        return true;
    }
    
    int size(int x) {
        return -par[root(x)];
    }
};

int main() {
    int n,m;
    cin>>n>>m;
    UnionFind tree(n+1);
    int a[n+1];
    rep(i,n){
        cin>>a[i+1];
    }
    rep(i,m){
        int x,y;
        cin>>x>>y;
        tree.merge(x,y);
    }
    int ans=0;
    for (int i=1;i<n+1;i++){
        if (tree.issame(i,a[i])){
            ans+=1;
        }
    }
  cout<<ans<<endl;

    return 0;
}