bool check (int fr) {
	int fuck = 0;
	for (int i = fr; i <= n; ++i) {
		fuck += a[i];
	}
	bool ok = 1;
	for (int i = fr - 1; i >= 1; --i) {
		if (fuck * 2 >= a[i]) {
			fuck += a[i];
		}
		else {
			ok = 0;
			break;
		}
	}
	return ok;
}

void solve() {
	cin >> n;
	for (int i = 1; i <= n; ++i) {
		cin >> a[i];
	}	
	sort (a + 1, a + n + 1);
	reverse (a + 1, a + n + 1);
	int l = 1, r = n;
	while (l < r) {
		int mid = (l + r + 1) / 2;
		if (check(mid)) l = mid;
		else r = mid - 1;
	}
	cout << l;
}

