int sqrtint(int v){
    int lo = 0;
    int hi = INT_MAX-2;
    while(hi-lo > 1){
        int mid = (lo+hi)/2;
        if (mid * mid <= v){
            lo = mid;
        } else {
            hi = mid;
        }
    }
    return lo;
}


