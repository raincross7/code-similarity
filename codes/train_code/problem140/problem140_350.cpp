int main(){
    int n,m;
    scanf("%d%d",&n,&m);
    vector<int> l(m);
    vector<int> r(m);
    for(int i=0;i<m;i++){
        scanf("%d%d",&l[i],&r[i]);
    }
    int max_l=l[0];
    int min_r=r[0];
    for(int i=1;i<m;i++){
        max_l=max(max_l,l[i]);
        min_r=min(min_r,r[i]);
    }
    if(max_l<=min_r){
        cout<<min_r-max_l+1<<"\n";
    }
    else{
        cout<<"0\n";
    }
    return 0;
}
